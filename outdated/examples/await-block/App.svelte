<script>
	let promise;

	function findAnswer() {
		promise = new Promise(fulfil => {
			const delay = 1000 + Math.random() * 3000;
			setTimeout(() => fulfil(42), delay);
		});
	}
</script>

<button on:click='{findAnswer}'>найти ответ</button>

{#if promise}
	{#await promise}
		<p>Думатель размышляет...</p>
	{:then answer}
		<p>ответ {answer}!</p>
	{:catch error}
		<p>а это весьма странно</p>
	{/await}
{/if}
